@Injectable()
export class ServerInterceptor implements HttpInterceptor {
  intercept(request: HttpRequest<any>, next: HttpHandler): Observable<HttpEvent<any>> {
    if (request.method === "GET") {
      console.log('data GET', data);
      return of(new HttpResponse({status: 200, body: data}));
    }

    if (request.method === "POST") {
      const cow = request.body;
      cow.cowId = nextCowId(data);
      data.push(cow);
      console.log('data POST', data);
      return of(new HttpResponse({status: 200, body: data}));
    }

    if (request.method === "DELETE") {
      const id = request.params.get('id');
      data.splice(data.findIndex(cow => cow.cowId === +id!), 1);
      console.log('data DELETE', data);
      return of(new HttpResponse({status: 200, body: data}));
    }
    if (request.method === "PUT") {
      const cow = request.body as Cow;
      Object.defineProperties(data.find(cows => cows.cowId === cow.cowId), {
        'healthIndex': {value: cow.healthIndex},
        'type': {value: cow.type},
        'animalId': {value: cow.animalId},
        'eventId': {value: cow.eventId},
        'lactationNumber': {value: cow.lactationNumber},
        'daysInLactation': {value: cow.daysInLactation},
        'ageInDays': {value: cow.ageInDays},
        'alertType': {value: cow.alertType},
        'duration': {value: cow.duration},
        'daysInPregnancy': {value: cow.daysInPregnancy},
        'heatIndexPeak': {value: cow.heatIndexPeak},
        'newGroupId': {value: cow.newGroupId},
        'newGroupName': {value: cow.newGroupName},
        'currentGroupId': {value: cow.currentGroupId},
        'currentGroupName': {value: cow.currentGroupName},
        'destinationGroup': {value: cow.destinationGroup},
        'destinationGroupName': {value: cow.destinationGroupName},
        'breedingNumber': {value: cow.breedingNumber},
        'interval': {value: cow.interval},
      });
      console.log('data PUT', data);
      return of(new HttpResponse({status: 200, body: data}));
    }
    return next.handle(request)
  }
}
